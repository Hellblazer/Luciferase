package com.hellblazer.luciferase.lucien.entity;

/**
 * Marker interface for entity identifiers in the Octree entity storage system.
 * Entity IDs are orthogonal to spatial Morton indices and provide stable
 * identity for objects across tree operations.
 * 
 * @author hal.hildebrand
 */
public interface EntityID {
    /**
     * Get a string representation suitable for debugging
     */
    String toDebugString();
}